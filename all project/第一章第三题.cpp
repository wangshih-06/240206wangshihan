#include<iostream>
using namespace std;
int main() {
	cout << "char length:" << sizeof(char) << endl;

	cout<<"int length:" << sizeof(int) << endl; 
	cout << "double length:" << sizeof(double) << endl;
	cout<< "float length:" << sizeof(float) << endl;
	cout << "long length:" << sizeof(long) << endl;
	return 0;
}